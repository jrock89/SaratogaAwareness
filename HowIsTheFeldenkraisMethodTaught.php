						<h1><a href="#" style="color: #2F2E2F;">How is the Feldenkrais Method taught?</a></h1><img class="img-responsive imgMove" src="images/feld2.jpg"> The Feldenkrais Method is differentiated it into two complementary formats: Awareness Through Movement (ATM) and Functional Integration (FI). The Awareness Through Movement format consists of structured movement lessons in which students are verbally guided through the exploration of their personal body relationships in a specific pattern of movement action (for example: crawling or turning over). In these lessons, essential human functions are broken down into their smallest components and looked at with relation to gravity. They are presented in a way in which the student is encouraged to be both investigative and observational while at the same time self-limiting; taking time, and taking in all variations of movement for their nervous system to discern, integrate and later have available for selection. These lessons are meant to be a conscious conversation between the student and their nervous system. With the combination of guided attention, and pleasant, purposeful movement in these Awareness Through Movement lessons, sensory learning is emphasized. The student learns how they can reduce unnecessary muscular effort and may realize the potential for simpler movement in a wider variety of situations in their life. Importantly, the student will gain increased awareness of their whole self in action - a concept that we rarely visit.<br>
						<br>
						The complement of this Awareness Through Movement practice is Functional Integration: a one-on-one approach to the method. In this format, learning, change and improvement in a student's functioning are produced through subtle yet specific and skilled passive manipulation by the practitioner, individualized for the student's particular needs. This work is generally done on a table or in a seated position and the student is fully clothed. In both of its forms, the Feldenkrais Method is geared toward learning and rediscovering individual potential for coordination, balance, flexibility, grace, and overall quality, ease, and efficiency of movement. The method only explores what is possible and comfortable for a particular individual at a particular moment. Thus, it allows that individual's nervous system to discern and better define their current limits of motion in a safe and positive atmosphere. These aspects of safe, pleasant movement and a safe atmosphere free of negative judgement or expectations is the key to learning about the self and key to successive improvement of all human functioning.<br>
