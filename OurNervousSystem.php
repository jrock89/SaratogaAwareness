						<h1><a href="#" style="color: #2F2E2F;">Our Nervous System: Built to Survive</a></h1><img class="img-responsive imgMove" src="images/nerv.jpg"> Even as we develop in the womb, our incredible body is establishing systems for survival. When we enter the world, we are tested for our survival reflexes: the Moro Reflex, or the spreading of the arms and then pulling in of the arms, usually followed by a cry in response to the sudden lack of support or feeling of falling; the Startle Response, or pulling in of the arms, in response to a loud noise; the instinct to grasp for a finger, root for a breast, and suck. These reflexes, all rapid, unlearned, involuntary responses, each have an evolutionary function in our survival, whether it be in protecting ourselves, defending ourselves, or nourishing ourselves. Our nervous system supplies the basis for this.<br>
						<br>
						But it is not only through reflexes that our nervous system functions to preserve our body's well-being. As we mature, we grow out of many of our infantile reflexes. Though other reflexes remain to protect us from injury or deal with things that require immediate reaction (coughing, blinking, sneezing, ducking, eye dilation, pulling away from a hot stimulus) we also begin learning how to protect, defend and nourish their bodies. So while our body’s amazing reflex arc pulls our hand from a hot stove even before we feel the pain, we learn that pain is associated with the hot stove. Our mother may yell at us and we learn to associate this fear, shame, etc. too with the touching of the hot stove. We form a memory of the stimulus and the emotions associated with it and we can consciously decide to be more careful when around a hot stove.<br>
						<br>
						Though some of these lessons are taught directly, often we are not aware of the associations our brain makes, thus unaware of our learning. So this is true for all learning. We learn to walk, talk, communicate and express ourselves, and although as adults it may feel like these have become automatic because we are familiar with the action, they are still in our voluntary control.<br>
						<br>
						So when we react explosively to a stimuli, or exhibit defensive behavior in an interaction, this is based on learning. We use the associations our brains have made in the past to preserve ourselves in future ‘harmful’ situations. Our brains use what they have learned, the connections that have been made, the memories and associated emotions to react quickly. It could be dangerous not to. It could take longer to reason and decision-make in the heat of the moment, so because timing is of the utmost importance in survival, our nervous system reacts without it.<br>
						<br>
						However, in this process we sometimes fear more than evidence warrants and sometimes less than the evidence warns. Sometimes our reactions are too big or too small. Our past learning and associations may not necessarily be the most appropriate basis for a current situation. For example, an individual’s brain scan responds similarly to a picture of an unpleasant situation, even without risk of danger, as it does to an actual unpleasant situation. Our nervous system prepares us for ‘fight or flight’ before we can think. According to the work of neuroscientist Joseph LeDoux of New York University, and others, the very architecture of the brain ensures that we “first feel and think second” sending information on incoming stimuli to the amygdala and the sympathetic nervous system before going to a part which can process and reason.<br>
						<br>
						This safety mechanism and our perception of danger are not the only influence on our nervous system to forgo conscious decision-making and instinctive behavior though. Sometimes it is simply the fact that we have more important things pay attention to in the moment than our already learned actions. For example if we are walking and carrying a baby, often we pay attention more to the more important action, carrying the baby, and allow our body to act as we have subconsciously taught it to in our walking. As a function of survival, we have to allow things to fall to our subconscious at times.<br>
						<br>
						Our nervous system is built to survive, defend and protect, but functions on the information it has received and the associations it has made. So the most amazing part of all or this is that we can always learn more. We can always make new associations in our brain which can govern our actions when they become subconscious in times of stress. Essentially we can function better and we can learn to do so.<br>
