						The Feldenkrais Method® is a deliberate conversation with an individual’s nervous system about its body’s functioning and efficiency in action. The language of this conversation is movement.<br>
						<br>
						The Feldenkrais Method® deepens an individual’s sense of how they move and how their whole body cooperates in movement (or doesn’t). It addresses certain critical human functions that are necessary for survival and explores all the variation potential in these functions. It brings a body to a neutral yet dynamic place in which all muscles may work together to support the skeleton in action: from which movement is easy, pleasurable, simple, coordinated, balanced and graceful. And most importantly it returns an individual to their dependable, reliable nervous system, breaking habituation and allowing for improvement in all human functioning to become possible in that individual.<br>
						<br>
						The Feldenkrais Method® is widely recognized for its ability to address minor aches and pains to chronic tension, serious muscular-skeletal and neurological problems, and the developmental problems of children. This method reconnects learning with human health and function – a principle that has innately guided all of our development since were born.
