							At Bodywork Professionals, it is my goal to reach the every-man (and every-woman). I have no major disabilities and my extraordinary talents are not as an athlete, dancer or musician, but I, like you, have a nervous system and an incredible amount of untapped potential. As we grow, we create our own self-image. We define our limits, our ‘reality’. But this has nothing to do with what is actually possible for us should we have the opportunity to learn more. With the Feldenkrais Method® nothing feels too far out of reach anymore. Of course I know that to become an incredible dancer, actor, musician or athlete it takes years of study, but if you know how to learn it is all possible.<br>
