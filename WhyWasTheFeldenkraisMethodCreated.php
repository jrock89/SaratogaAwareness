						<h1><a href="#" style="color: #2F2E2F;">Why was the Feldenkrais Method created?</a></h1><img class="img-responsive imgMove" src="images/feld1.jpg"> The Feldenkrais Method was developed by Moshe Feldenkrais, D.Sc. of Israel in the 1940's and introduced to the United States in the early 1970's. Dr. Feldenkrais was a distinguished scientist, physicist, engineer, and Judo instructor driven by a personal crippling injury to become concentrated on the exploration of the relationships between movement, thinking, feeling, learning and healing. He thoroughly educated himself with any course of study that he came across, from Freud's psychology to various movement therapies and rubbed elbows with some of the most renowned thinkers of his time. Some could certainly call this man a genius. Through his studies, Moshe formulated a method that not only helped him overcome his own injury but which also could be applied to every human who functioned with a nervous system (which is every human that I have ever come across).<br>
						<br>
						Dr. Moshe Feldenkrais merged his insights from physics, motor development, bio-mechanics, psychology, and martial arts in the creation of his method. He developed a practice of quiet and exploratory movement awareness directed by a process rather than a goal. This lack of goal orientation allowed the participant to really learn through the process and receive guidance to a state where their movement and their functioning as a whole could become more efficient, coordinated, precise and spontaneous - a state where full dynamic living and health were possibilities.<br>
						<br>
						And thus the Feldenkrais Method was born - named only because he had to, proving that ‘necessity is the mother of invention’.
